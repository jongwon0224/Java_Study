package study.practice.prac19;

public class Student {
	
	private String dept; // 학과
	private int deptNo; //학번
	
	
	// dept setter
	public void setDept (String dept) {
		this.dept = dept;
	}
	
	// deptno setter
	public void setDeptNo (int deptNo) {
		this.deptNo = deptNo;
	}
	
	// dept getter
	public String getDept() {
		return dept;
	}
	// deptno getter
	public int getDeptNo () {
		return deptNo;
	}



	
}
